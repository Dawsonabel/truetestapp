"use client";

import { useState, useEffect, Suspense } from 'react';
import { useRouter, useSearchParams } from 'next/navigation';
import styles from 'app/onboard/styles/onboard.module.css';
import jwt from 'jsonwebtoken';

// Create a wrapper component that uses useSearchParams
function OnboardContentWithParams() {
  const searchParams = useSearchParams();
  return <OnboardContent searchParams={searchParams} />;
}

// Modify OnboardContent to accept searchParams as a prop
function OnboardContent({ searchParams }) {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [error, setError] = useState('');
  const [isLoading, setIsLoading] = useState(true);
  const router = useRouter();

  const [tokenData, setTokenData] = useState(null);
  const [tokenError, setTokenError] = useState(null);

  useEffect(() => {
    const verifyToken = async () => {
      try {
        const token = searchParams.get('token');
        if (!token) {
          setTokenError('No token provided');
          return;
        }

        const response = await fetch('/.netlify/functions/verify-token', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
          },
          body: JSON.stringify({ token }),
        });

        const data = await response.json();

        if (!response.ok) {
          throw new Error(data.message || 'Token verification failed');
        }

        setTokenData({
          answers: data.decoded.answers,
          price: data.decoded.price,
          email: data.decoded.email,
          paymentIntentId: data.decoded.paymentIntentId
        });
        
        setEmail(data.decoded.email);

        if (window.twq) {
          console.log('Firing Twitter conversion event with data:', {
            value: data.decoded.price,
            currency: 'USD',
            email_address: data.decoded.email
          });
          
          window.twq('event', 'tw-osome-osomf', {
            value: data.decoded.price,
            currency: 'USD',
            email_address: data.decoded.email
          });
        } else {
          console.log('Twitter pixel not loaded yet');
        }

      } catch (error) {
        console.error('Token verification failed:', error);
        setTokenError(error.message);
      } finally {
        setIsLoading(false);
      }
    };

    verifyToken();
  }, [searchParams]);

  const handleSubmit = async (e) => {
    e.preventDefault();
    setError('');

    const userEmail = tokenData?.email || email;
    const userName = userEmail.split('@')[0];

    if (!userEmail || !password) {
      setError('Please enter both email and password');
      return;
    }

    // Create the payload with the correct payment intent ID from token
    const payload = {
      email: userEmail.trim(),
      password: password.trim(),
      name: userName,
      paymentIntentId: tokenData?.paymentIntentId,
      quizAnswers: tokenData?.answers,
      price: tokenData?.price
    };

    try {
      const response = await fetch('/.netlify/functions/create-user', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify(payload),
      });

      const data = await response.json();

      if (!response.ok) {
        console.error('Server error details:', data);
        throw new Error(data.message || `Server error: ${response.status}`);
      }

      // Twitter conversion tracking event code - exactly as provided by Twitter
      if (window.twq) {
        window.twq('event', 'tw-osome-osomf', {
          value: tokenData?.price || null,
          currency: 'USD',
          email_address: userEmail || null
        });
      }

      router.push('/login');
    } catch (error) {
      console.error('Registration error:', error);
      setError(error.message || 'An error occurred during registration');
    }
  };

  if (isLoading) {
    return (
      <div className={styles.loadingContainer}>
        <div className={styles.loadingSpinner}>
          <svg viewBox="0 0 50 50" className={styles.spinner}>
            <circle cx="25" cy="25" r="20" fill="none" stroke="currentColor" strokeWidth="4"/>
          </svg>
        </div>
      </div>
    );
  }

  if (tokenError) {
    return (
      <div className={styles.errorContainer}>
        <div className={styles.glassCard}>
          <svg className={styles.errorIcon} viewBox="0 0 24 24">
            <path d="M12 2C6.48 2 2 6.48 2 12s4.48 10 10 10 10-4.48 10-10S17.52 2 12 2zm1 15h-2v-2h2v2zm0-4h-2V7h2v6z" />
          </svg>
          <p className={styles.error}>Invalid or expired token</p>
          <p>{tokenError}</p>
        </div>
      </div>
    );
  }

  return (
    <div className={styles.container}>
      <div className={styles.successHeader}>
        <svg className={styles.checkIcon} viewBox="0 0 24 24">
          <path d="M9 16.17L4.83 12l-1.42 1.41L9 19 21 7l-1.41-1.41L9 16.17z" />
        </svg>
        <h2 className={styles.successText}>Success!</h2>
      </div>
      <div className={styles.glassCard}>
        <h1 className={styles.title}>Complete Your Registration</h1>
        {tokenData?.answers['16']?.q16 && (
          <div className={styles.emailDisplay}>
            <svg className={styles.userIcon} viewBox="0 0 24 24">
              <path d="M12 12c2.21 0 4-1.79 4-4s-1.79-4-4-4-4 1.79-4 4 1.79 4 4 4zm0 2c-2.67 0-8 1.34-8 4v2h16v-2c0-2.66-5.33-4-8-4z" />
            </svg>
            <p>{tokenData.answers['16'].q16}</p>
          </div>
        )}
        <form onSubmit={handleSubmit} className={styles.form}>
          {!tokenData?.answers['16']?.q16 && (
            <input
              type="email"
              placeholder="Email"
              value={email}
              onChange={(e) => setEmail(e.target.value)}
              className={styles.input}
              required
            />
          )}
          <input
            type="password"
            placeholder="Choose a password"
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            className={styles.input}
            required
          />
          <button type="submit" className={styles.button}>
            Complete Registration
          </button>
        </form>
        {error && (
          <div className={styles.errorMessage}>
            <svg className={styles.errorIcon} viewBox="0 0 24 24">
              <path d="M12 2C6.48 2 2 6.48 2 12s4.48 10 10 10 10-4.48 10-10S17.52 2 12 2zm1 15h-2v-2h2v2zm0-4h-2V7h2v6z" />
            </svg>
            <p>{error}</p>
          </div>
        )}
      </div>
      <p className={styles.supportNotice}>
        Need to change your email or other issues?<br></br> Please contact support{' '}
        <a href="mailto:info@truetest.pro" className={styles.supportEmail}>
          info@truetest.pro
        </a>
      </p>
    </div>
  );
}

// Update the default export to include Suspense
export default function Register() {
  return (
    <Suspense fallback={<div>Loading...</div>}>
      <OnboardContentWithParams />
    </Suspense>
  );
}
